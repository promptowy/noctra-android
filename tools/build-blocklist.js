#!/usr/bin/env node
/**
 * Generates app/src/main/assets/blocklist.txt from EasyList + EasyPrivacy + Polish list.
 * Run: node tools/build-blocklist.js
 * Requires Node.js >= 18 (fetch built-in) or earlier with node-fetch.
 */

const fs = require('fs');
const path = require('path');

const SOURCES = [
  'https://easylist.to/easylist/easylist.txt',
  'https://easylist.to/easylist/easyprivacy.txt',
  'https://easylist-downloads.adblockplus.org/easylistpolish.txt',
  'https://secure.fanboy.co.nz/fanboy-cookiemonster.txt',
];

const OUT = path.join(__dirname, '..', 'app', 'src', 'main', 'assets', 'blocklist.txt');

async function fetchText(url) {
  console.log('  fetching', url);
  const res = await fetch(url);
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${url}`);
  return res.text();
}

// Parse an EasyList/ABP rule and extract the domain if it's a plain domain block.
// Returns domain string or null.
function extractDomain(rule) {
  const line = rule.trim();
  // Skip comments, exceptions (@@), element hiding (##), options with complex patterns
  if (!line || line.startsWith('!') || line.startsWith('[') || line.startsWith('@@')) return null;
  if (line.includes('##') || line.includes('#@#') || line.includes('#?#')) return null;

  // ||domain^ or ||domain^$options  — the canonical domain rule format
  const m = line.match(/^\|\|([a-z0-9._-]+)\^/i);
  if (m) {
    const domain = m[1].toLowerCase().replace(/^www\./, '');
    // Sanity: must look like a real domain
    if (domain.includes('.') && !domain.startsWith('.') && domain.length < 100) return domain;
  }
  return null;
}

async function main() {
  const domains = new Set();

  for (const url of SOURCES) {
    try {
      const text = await fetchText(url);
      let added = 0;
      for (const line of text.split('\n')) {
        const d = extractDomain(line);
        if (d) { domains.add(d); added++; }
      }
      console.log(`  → ${added} domains added`);
    } catch (e) {
      console.error('  WARN: failed to fetch', url, e.message);
    }
  }

  fs.mkdirSync(path.dirname(OUT), { recursive: true });
  fs.writeFileSync(OUT, '# Noctra Android blocklist — generated by tools/build-blocklist.js\n# ' +
    new Date().toISOString() + '\n' +
    [...domains].sort().join('\n') + '\n');

  console.log(`\n✓ Written ${domains.size} domains to ${OUT}`);
}

main().catch(e => { console.error(e); process.exit(1); });
